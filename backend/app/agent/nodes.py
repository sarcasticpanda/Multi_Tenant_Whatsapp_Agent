def _build_system_prompt(tenant: dict, rag_chunks: list) -> str:
    prompt = tenant["system_prompt"]

    # Tell the LLM EXACTLY what media files exist so it never promises
    # or re-sends something it doesn't have.
    media_lib = tenant.get("media_library", {})
    if media_lib:
        # Deduplicate by URL (e.g. 'catalog' and 'brochure' may share one file)
        seen_urls = {}
        for keyword, url in media_lib.items():
            seen_urls.setdefault(url, []).append(keyword)
        prompt += "\n\n--- MEDIA YOU CAN SEND (via get_media tool) ---\n"
        for url, keywords in seen_urls.items():
            kind = "PDF document" if url.lower().endswith(".pdf") else "image"
            prompt += f"- {kind}: ask with keyword '{keywords[0]}'\n"
        prompt += (
            "This is the COMPLETE list of files you have. You have exactly ONE file per item above.\n"
            "If a customer asks for 'more' images or something not in this list, do NOT re-send the same "
            "file. Instead, honestly say that's the piece you have on hand and offer the full *catalog* "
            "to see the complete range. Only call get_media when the customer actually wants to receive a file.\n"
            "--- END MEDIA LIST ---\n"
        )

    if rag_chunks:
        prompt += "\n\n--- RELEVANT KNOWLEDGE BASE ---\n"
        for i, chunk in enumerate(rag_chunks, 1):
            prompt += f"\n[{i}] {chunk}\n"
        prompt += "\n--- END KNOWLEDGE BASE ---\n"
        prompt += (
            "\nBase your answer on the knowledge base above. "
            "Do not fabricate prices, specs, or policies not mentioned."
        )
    return prompt


async def llm_reasoning_node(state: AgentState) -> AgentState:
    """
    Calls Gemini 2.5 Flash with tool calling.
    Handles get_media, search_knowledge, escalate_to_human tools.
    """
    if state.get("error"):
        state["llm_reply"] = "Sorry, I'm having technical difficulties. Please try again shortly."
        return state

    tenant = state["tenant_config"]
    system_prompt = _build_system_prompt(tenant, state.get("rag_chunks") or [])

    # Build conversation history for Gemini new SDK
    contents = []
    for msg in (state.get("chat_history") or [])[:-1]:
        role = "user" if msg["direction"] == "INBOUND" else "model"
        text = msg.get("text_content", "")
        if text:
            contents.append(types.Content(role=role, parts=[types.Part.from_text(text=text)]))

    # Current user message
    user_text = state["inbound_text"]
    if state.get("inbound_image_description"):
        user_text = (
            f"[Customer sent an image: {state['inbound_image_description']}]\n"
            f"{user_text}"
        )
    contents.append(types.Content(role="user", parts=[types.Part.from_text(text=user_text)]))

    try:
        response = _gemini_client.models.generate_content(
            model=GEMINI_MODEL,
            contents=contents,
            config=types.GenerateContentConfig(
                system_instruction=system_prompt,
                tools=_gemini_tools,
                temperature=0.4,
                automatic_function_calling=types.AutomaticFunctionCallingConfig(disable=True),
            ),
        )
    except Exception as e:
        logger.error(f"Gemini call failed: {e} -> trying Groq fallback")
        # Fallback: Groq for a plain-text reply (no media tools) so the bot stays alive
        fallback = _groq_text_fallback(system_prompt, state)
        state["llm_reply"] = fallback or "I'm experiencing a brief outage. Please try again in a moment."
        state["media_to_send"] = None
        state["media_type"] = None
        state["media_filename"] = None
        return state

    # Process response parts
    final_reply = None
    media_url = None
    media_type = None
    media_filename = None
    function_response_parts = []  # for the 2nd call (natural caption)

    candidate_parts = response.candidates[0].content.parts
    for part in candidate_parts:
        if part.function_call:
            fn = part.function_call
            fn_args = dict(fn.args) if fn.args else {}

            if fn.name == "get_media":
                keyword = fn_args.get("keyword", "").lower()
                logger.info(f"[LLM DECISION] called tool get_media(keyword={keyword!r}) -> looking up MongoDB media_library")
                media_lib = tenant.get("media_library", {})
                matched_key = None
                for key, url in media_lib.items():
                    if keyword in key.lower() or key.lower() in keyword:
                        media_url = url
                        matched_key = key
                        if url.lower().endswith(".pdf"):
                            media_type = "DOCUMENT"
                            media_filename = f"{key.title().replace(' ', '_')}.pdf"
                        else:
                            media_type = "IMAGE"
                        logger.info(f"[MEDIA LIBRARY] matched {key!r} -> {media_type}: {url}")
                        break
                # get_media outcome is deterministic — write a contextual caption locally.
                # This deliberately AVOIDS a 2nd Gemini call (saves the free-tier budget).
                if matched_key:
                    pretty = matched_key.title()
                    if media_type == "DOCUMENT":
                        final_reply = f"Here's our *{pretty}* 📄 — take a look and tell me what catches your eye!"
                    else:
                        final_reply = f"Here's the *{pretty}* 😊 Let me know if you'd like to see more."
                else:
                    final_reply = (
                        "That's the piece I have on hand right now. "
                        "Would you like me to send our full *catalog* to see the complete range?"
                    )

            elif fn.name == "search_knowledge":
                extra_query = fn_args.get("query", "")
                extra = search_knowledge_base(extra_query, state["tenant_id"])
                existing = state.get("rag_chunks") or []
                state["rag_chunks"] = existing + [c for c in extra if c not in existing]
                function_response_parts.append(
                    types.Part.from_function_response(
                        name="search_knowledge",
                        response={"results": extra[:3] if extra else "no additional info found"},
                    )
                )

            elif fn.name == "escalate_to_human":
                state["session_status"] = "NEEDS_HUMAN"
                final_reply = (
                    "I completely understand, and I'm so sorry for the frustration. "
                    "I'm bringing in one of our human specialists right now to personally take care of "
                    "this for you. They'll reach out very shortly. 🙏"
                )

        elif part.text:
            final_reply = part.text

    # Two-step function calling: if a tool ran, ask Gemini to write a natural reply
    if function_response_parts and not final_reply:
        try:
            contents.append(response.candidates[0].content)            # model's tool-call turn
            contents.append(types.Content(role="user", parts=function_response_parts))  # tool results
            followup = _gemini_client.models.generate_content(
                model=GEMINI_MODEL,
                contents=contents,
                config=types.GenerateContentConfig(
                    system_instruction=system_prompt,
                    temperature=0.5,
                ),
            )
            final_reply = followup.text
        except Exception as e:
            logger.warning(f"Caption follow-up failed: {e}")

    # Last-resort fallback (only if everything above produced nothing)
    if not final_reply:
        if media_type == "DOCUMENT":
            final_reply = "Here you go! 📄 Let me know if anything catches your eye."
        elif media_type == "IMAGE":
            final_reply = "Here it is! 😊 Want to see more pieces? I can share our full catalog."
        else:
            final_reply = "I'm here to help! Could you tell me a bit more about what you're looking for?"

    state["llm_reply"] = final_reply
    state["media_to_send"] = media_url
    state["media_type"] = media_type
    state["media_filename"] = media_filename

    logger.info(f"[REPLY] {state['llm_reply'][:150]!r}")
    if media_url:
        logger.info(f"[REPLY] + attaching {media_type}: {media_filename or media_url}")
    logger.info(f"[STATUS] session -> {state['session_status']}")

    return state


