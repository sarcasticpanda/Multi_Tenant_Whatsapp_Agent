import base64
import logging
from datetime import datetime
from uuid import uuid4

from google import genai
from google.genai import types

from app.agent.state import AgentState
from app.agent.tools import TOOLS
from app.config import settings
from app.db.mongodb import get_db
from app.rag.chroma_client import search_knowledge_base
from app.whatsapp import client as wa

logger = logging.getLogger(__name__)

_gemini_client = genai.Client(api_key=settings.gemini_api_key)
GEMINI_MODEL = settings.gemini_model

# Optional Groq fallback client (used only if Gemini fails / rate-limits)
_groq_client = None
if settings.groq_api_key:
    try:
        from groq import Groq
        _groq_client = Groq(api_key=settings.groq_api_key)
    except Exception as e:
        logger.warning(f"Groq fallback unavailable: {e}")


def _groq_text_fallback(system_prompt: str, state: AgentState) -> str | None:
    """Plain-text reply via Groq when Gemini is unavailable. No tool calling."""
    if not _groq_client:
        return None
    try:
        msgs = [{"role": "system", "content": system_prompt}]
        for m in (state.get("chat_history") or [])[:-1]:
            role = "user" if m["direction"] == "INBOUND" else "assistant"
            if m.get("text_content"):
                msgs.append({"role": role, "content": m["text_content"]})
        msgs.append({"role": "user", "content": state["inbound_text"]})

        resp = _groq_client.chat.completions.create(
            model=settings.groq_model,
            messages=msgs,
            temperature=0.5,
            max_tokens=400,
        )
        logger.info("[FALLBACK] reply generated by Groq")
        return resp.choices[0].message.content
    except Exception as e:
        logger.error(f"Groq fallback failed: {e}")
        return None

# Convert our tool defs to Gemini types.Tool format
_gemini_tools = [
    types.Tool(function_declarations=[
        types.FunctionDeclaration(
            name=t["name"],
            description=t["description"],
            parameters=types.Schema(
                type=types.Type.OBJECT,
                properties={
                    k: types.Schema(type=types.Type.STRING, description=v.get("description", ""))
                    for k, v in t["parameters"]["properties"].items()
                },
                required=t["parameters"].get("required", []),
            ),
        )
        for t in TOOLS
    ])
]


